export const getWorkBySlug = (slug) =>
  artWorks.find((work) => work.slug === slug || work.aliases?.includes(slug));

export const getCanonicalWorkSlug = (slug) => getWorkBySlug(slug)?.slug || slug;

export const getAdjacentWorks = (slug) => {
  const canonicalSlug = getCanonicalWorkSlug(slug);
  const index = artWorks.findIndex((work) => work.slug === canonicalSlug);
  if (index === -1) return { previous: null, next: null };

  return {
    previous: artWorks[index - 1] || artWorks[artWorks.length - 1],
    next: artWorks[index + 1] || artWorks[0],
  };
};
